const fs = require(`fs`);
const path = require(`path`);
const { execSync } = require(`child_process`);

const rootDir = path.resolve(__dirname, `..`);
const outputPath = path.resolve(rootDir, `src/utils/generatedBuildInfo.ts`);

function runCommand(command) {
    try {
        return execSync(command, {
            cwd: rootDir,
            encoding: `utf8`,
            stdio: [`ignore`, `pipe`, `ignore`],
        }).trim();
    } catch (_) {
        return ``;
    }
}

const shortHash = runCommand(`git rev-parse --short HEAD`) || `unknown`;
const fullHash = runCommand(`git rev-parse HEAD`) || `unknown`;
const branch = runCommand(`git rev-parse --abbrev-ref HEAD`) || `unknown`;
const workingTreeStatus = runCommand(`git status --porcelain`);
const dirtyState = workingTreeStatus ? `dirty` : `clean`;
const branchLabel = branch && branch !== `HEAD` ? `${branch}@` : ``;
const buildLabel = `${branchLabel}${shortHash}${dirtyState === `dirty` ? `*` : ``}`;

const generatedFileContents = `// This file is auto-generated by scripts/write-build-info.js.
// Do not edit this file manually.
export const BUILD_REVISION = ${JSON.stringify(shortHash)};
export const BUILD_FULL_REVISION = ${JSON.stringify(fullHash)};
export const BUILD_BRANCH = ${JSON.stringify(branch)};
export const BUILD_WORKTREE_STATE = ${JSON.stringify(dirtyState)};
export const BUILD_LABEL = ${JSON.stringify(buildLabel)};
`;

fs.mkdirSync(path.dirname(outputPath), { recursive: true });

let currentContents = ``;
if (fs.existsSync(outputPath)) {
    currentContents = fs.readFileSync(outputPath, `utf8`);
}

if (currentContents !== generatedFileContents) {
    fs.writeFileSync(outputPath, generatedFileContents, `utf8`);
    console.log(`Updated build info: ${buildLabel}`);
} else {
    console.log(`Build info unchanged: ${buildLabel}`);
}
